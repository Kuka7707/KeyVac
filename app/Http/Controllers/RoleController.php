     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
      $roles = Role::all();
         $data = [];
         foreach ($roles as $value) {
            $data [] = [
               'id' => $value->id,
               'name' => $value->name,
               'permission' => [
                  'read' => [
                     'name' => 'Чтение',
                     'status' => $value->read,
                  ],
                  'edit' => [
                     'name' => 'Редактирование',
                     'status' => $value->edit,
                  ],
                  'del' => [
                     'name' => 'Удаление',
                     'status' => $value->delete,
                  ],
               ]
            ];
         }
         $roles = collect($data);
         return response()->json($roles);
    }

